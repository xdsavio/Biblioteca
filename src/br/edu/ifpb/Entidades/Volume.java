package br.edu.ifpb.Entidades;

public class Volume {
	
	private String titulo;
	private String autor;
	private String descricao;
	private String tipo;
	private String num_pag;
	private String ano_public;
	private String editora;

	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public String getAutor() {
		return autor;
	}
	public void setAutor(String autor) {
		this.autor = autor;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getNum_pag() {
		return num_pag;
	}
	public void setNum_pag(String num_pag) {
		this.num_pag = num_pag;
	}
	public String getAno_public() {
		return ano_public;
	}
	public void setAno_public(String ano_public) {
		this.ano_public = ano_public;
	}
	public String getEditora() {
		return editora;
	}
	public void setEditora(String editora) {
		this.editora = editora;
	}

}
